// ==UserScript==
// @name         myTE Period Approval EML Auto Generator (Images Only)
// @namespace    http://tampermonkey.net/
// @version      2.1
// @description  Summary/Time/Expenses/Adjustments の画面キャプチャだけを .eml に埋め込む
// @match        https://myte.accenture.com/*
// @require      https://cdn.jsdelivr.net/npm/html2canvas@1.4.1/dist/html2canvas.min.js
// @grant        none
// ==/UserScript==

(function () {
  'use strict';

  const BASE_MAIL_CONFIG = {
    from: '',//例：abc@gmail.com
    to: '',//例：abc@gmail.com
    cc: [
      '',//例：abc@gmail.com
      '',//例：abc@gmail.com
      '',//例：abc@gmail.com
    ],
    period: '',
    displayName: 'XXXX',//例：山田太郎
  };

  const NAV_STEPS = [
    {
      name: 'Summary',
      cid: 'cid-summary-image',
      selector: 'myte-app',
      navigate: async () => {
        const tab = document.getElementById('summary');
        if (tab) tab.click();
        await sleep(2000);
      },
    },
    {
      name: 'Time',
      cid: 'cid-time-image',
      selector: 'myte-app',
      navigate: async () => {
        const tab = document.getElementById('time');
        if (tab) tab.click();
        await sleep(2000);
      },
    },
    {
      name: 'Expenses',
      cid: 'cid-expenses-image',
      selector: 'myte-app',
      navigate: async () => {
        const tab = document.getElementById('expenses');
        if (tab) tab.click();
        await sleep(2000);
      },
    },
    {
      name: 'Adjustments',
      cid: 'cid-adjustments-image',
      selector: 'myte-app',
      navigate: async () => {
        const tab = document.getElementById('adjustments');
        if (tab) tab.click();
        await sleep(2000);
      },
    },
  ];

  function sleep(ms) {
    return new Promise((resolve) => setTimeout(resolve, ms));
  }

  function getPeriodFromPage() {
    const el = document.querySelector('div.item.active');
    if (!el) throw new Error('期間表示の要素 (div.item.active) が見つかりません。');
    const text = el.textContent.trim();
    if (!text) throw new Error('期間表示の要素からテキストが取得できませんでした。');
    const m = text.match(/^(\d{4})\/(\d{1,2})\/(\d{1,2})$/);
    if (!m) return text;
    const [, y, mo, d] = m;
    return `${y}/${mo.padStart(2, '0')}/${d.padStart(2, '0')}`;
  }

  async function captureElementAsBase64Png(selector) {
    const el = document.querySelector(selector);
    if (!el) throw new Error('キャプチャ対象の要素が見つかりません: ' + selector);
    const canvas = await html2canvas(el, {
      scale: 1.5,
      useCORS: true,
      logging: false,
    });
    const dataUrl = canvas.toDataURL('image/png');
    return dataUrl.split(',')[1];
  }

  function wrapBase64ForMime(str) {
    return str.replace(/(.{76})/g, '$1\r\n');
  }

  function formatDateForHeader(date) {
    return date.toUTCString();
  }

  function buildPlainTextBody(config) {
    return [
      'Hi, it would be great if you could give me your approval on this period.',
      '',
      'Please see the images in this message for Summary, Time, Expenses and Adjustments.',
      '',
      'Regards,',
      config.displayName,
      '',
      'Generated by Tampermonkey script for myTE',
      '',
    ].join('\r\n');
  }

  function buildHtmlBody(config, navResults) {
    const imgFor = (name, cid) =>
      `<div style="text-align: center; margin: 0mm; font-family: Calibri, Helvetica, sans-serif; font-size: 11pt; color: rgb(0, 0, 0);">
<img style="max-width: 432px;" alt="${name}" src="cid:${cid}"></div>`;

    const summaryStep = navResults.find((x) => x.name === 'Summary');
    const timeStep = navResults.find((x) => x.name === 'Time');
    const expensesStep = navResults.find((x) => x.name === 'Expenses');
    const adjustmentsStep = navResults.find((x) => x.name === 'Adjustments');

    return (
      '<html><head>' +
      '<meta http-equiv="Content-Type" content="text/html; charset=utf-8">' +
      '<style type="text/css" style="display:none;"> P {margin-top:0;margin-bottom:0;} </style>' +
      '</head><body dir="ltr">' +
      '<div style="margin: 0mm; font-family: Calibri, Helvetica, sans-serif; font-size: 11pt; color: rgb(0, 0, 0);">' +
      'Hi, it would be great if you could give me your approval on this period.' +
      '</div>' +
      '<div style="margin: 0mm; font-family: Calibri, Helvetica, sans-serif; font-size: 11pt; color: rgb(0, 0, 0);">&nbsp;</div>' +
      '<div><b>Summary</b></div>' +
      (summaryStep ? imgFor('Summary', summaryStep.cid) : '') +
      '<div>&nbsp;</div>' +
      '<div><b>Time</b></div>' +
      (timeStep ? imgFor('Time', timeStep.cid) : '') +
      '<div>&nbsp;</div>' +
      '<div><b>Expenses</b></div>' +
      (expensesStep ? imgFor('Expenses', expensesStep.cid) : '') +
      '<div>&nbsp;</div>' +
      '<div><b>Adjustments</b></div>' +
      (adjustmentsStep ? imgFor('Adjustments', adjustmentsStep.cid) : '') +
      '<div>Regards,<br>' +
      config.displayName +
      '</div>' +
      '<div>&nbsp;</div>' +
      '</body></html>'
    );
  }

  function buildEml(config, navResults) {
    const boundaryRelated = '_rel_' + Date.now();
    const boundaryAlt = '_alt_' + Date.now();
    const dateHeader = formatDateForHeader(new Date());
    const subject = `[myTE] ${config.period} Period Approval Request from ${config.displayName}`;

    const plainBody = buildPlainTextBody(config);
    const htmlBody = buildHtmlBody(config, navResults);

    let eml = '';

    eml += `From: ${config.from}\r\n`;
    eml += `To: ${config.to}\r\n`;
    if (config.cc && config.cc.length) eml += `CC: ${config.cc.join(', ')}\r\n`;
    eml += `Subject: ${subject}\r\n`;
    eml += `Date: ${dateHeader}\r\n`;
    eml += 'MIME-Version: 1.0\r\n';
    eml += `Content-Type: multipart/related; boundary="${boundaryRelated}"; type="multipart/alternative"\r\n`;
    eml += 'Content-Language: ja-JP\r\n\r\n';

    // multipart/alternative
    eml += `--${boundaryRelated}\r\n`;
    eml += `Content-Type: multipart/alternative; boundary="${boundaryAlt}"\r\n\r\n`;

    // text/plain
    eml += `--${boundaryAlt}\r\n`;
    eml += 'Content-Type: text/plain; charset="utf-8"\r\n\r\n';
    eml += plainBody + '\r\n';

    // text/html
    eml += `--${boundaryAlt}\r\n`;
    eml += 'Content-Type: text/html; charset="utf-8"\r\n\r\n';
    eml += htmlBody + '\r\n';

    eml += `--${boundaryAlt}--\r\n`;

    // 4タブ分を画像として添付
    for (const step of navResults) {
      if (!step.base64) continue;

      eml += `--${boundaryRelated}\r\n`;
      eml += `Content-Type: image/png; name="${step.name}.png"\r\n`;
      eml += `Content-Description: ${step.name}.png\r\n`;
      eml += `Content-Disposition: inline; filename="${step.name}.png"\r\n`;
      eml += `Content-ID: <${step.cid}>\r\n`;
      eml += 'Content-Transfer-Encoding: base64\r\n\r\n';
      eml += wrapBase64ForMime(step.base64) + '\r\n';
    }

    eml += `--${boundaryRelated}--\r\n`;

    return eml;
  }

  function downloadEml(filename, emlText) {
    const blob = new Blob([emlText], { type: 'message/rfc822' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = filename;
    document.body.appendChild(a);
    a.click();
    a.remove();
    URL.revokeObjectURL(url);
  }

  async function mainGenerateEml() {
    try {
      const period = getPeriodFromPage();
      const config = { ...BASE_MAIL_CONFIG, period };

      const results = [];
      for (const step of NAV_STEPS) {
        await step.navigate();
        const base64 = await captureElementAsBase64Png(step.selector);
        results.push({ ...step, base64 });
      }

      const emlText = buildEml(config, results);
      const filename = `[myTE] ${config.period} Period Approval Request from ${config.displayName}.eml`;
      downloadEml(filename, emlText);
      alert('EML ファイルを生成しました。');
    } catch (e) {
      console.error(e);
      alert('EML 生成中にエラーが発生しました: ' + e.message);
    }
  }

  function setupShortcutTrigger() {
    window.addEventListener('keydown', (e) => {
      // Alt + Shift + E で起動（入力中は無効化）
      const tag = (e.target && e.target.tagName) || '';
      if (tag === 'INPUT' || tag === 'TEXTAREA' || e.isComposing) return;

      if (e.altKey && e.shiftKey && e.code === 'KeyE') {
        e.preventDefault();
        e.stopPropagation();
        mainGenerateEml();
      }
    });
  }

  window.addEventListener('load', () => {
    setupShortcutTrigger();
    console.log('myTE EML Auto Generator (images only, Alt+Shift+E) loaded.');
  });
})();